#!/usr/bin/env node
import { cpSync, mkdirSync, readdirSync, rmSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

function usage() {
  console.log(`Usage: node scripts/stage-bundled-skills.mjs [options]

Copies hermes-agent-cn bundled skills into static/bundled-skills so Tauri
bundles them into the desktop installer. The desktop then syncs these files
into the managed runtime and exposes them to hermes dashboard via
HERMES_BUNDLED_SKILLS.

Options:
  --source <dir>      hermes-agent-cn checkout (default: ../hermes-agent-cn)
  --skills <dir>      Existing skills directory to copy
  --out <dir>         Output dir (default: static/bundled-skills)
`);
}

function argValue(flag, fallback = null) {
  const index = process.argv.indexOf(flag);
  if (index === -1) return fallback;
  const value = process.argv[index + 1];
  if (!value || value.startsWith("--")) {
    throw new Error(`${flag} requires a value`);
  }
  return value;
}

function hasFlag(flag) {
  return process.argv.includes(flag);
}

if (hasFlag("--help") || hasFlag("-h")) {
  usage();
  process.exit(0);
}

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const sourceRoot = resolve(
  repoRoot,
  argValue("--source", process.env.HERMES_AGENT_CN_SOURCE ?? "../hermes-agent-cn"),
);
const sourceSkills = resolve(
  repoRoot,
  argValue("--skills", process.env.HERMES_BUNDLED_SKILLS_SOURCE ?? join(sourceRoot, "skills")),
);
const outDir = resolve(repoRoot, argValue("--out", "static/bundled-skills"));

function capture(command, args, options = {}) {
  const result = spawnSync(command, args, {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
    shell: false,
    ...options,
  });
  if (result.status !== 0) return "";
  return result.stdout.trim();
}

function isDirectory(path) {
  try {
    return statSync(path).isDirectory();
  } catch {
    return false;
  }
}

function containsSkillMarkdown(path) {
  if (!isDirectory(path)) return false;
  for (const entry of readdirSync(path, { withFileTypes: true })) {
    const full = join(path, entry.name);
    if (entry.isDirectory()) {
      if (containsSkillMarkdown(full)) return true;
    } else if (entry.name.toLowerCase() === "skill.md") {
      return true;
    }
  }
  return false;
}

if (!containsSkillMarkdown(sourceSkills)) {
  throw new Error(`bundled skills source is missing SKILL.md files: ${sourceSkills}`);
}
if (resolve(sourceSkills) === resolve(outDir)) {
  throw new Error("source skills and output directory are the same path");
}

mkdirSync(dirname(outDir), { recursive: true });
rmSync(outDir, { recursive: true, force: true });
cpSync(sourceSkills, outDir, { recursive: true });

const sourceCommit = isDirectory(join(sourceRoot, ".git"))
  ? capture("git", ["rev-parse", "HEAD"], { cwd: sourceRoot })
  : "";
writeFileSync(
  join(outDir, "README.generated.txt"),
  [
    "Generated by scripts/stage-bundled-skills.mjs.",
    `source=${sourceRoot}`,
    sourceCommit ? `sourceCommit=${sourceCommit}` : null,
    `skills=${sourceSkills}`,
    "",
  ]
    .filter(Boolean)
    .join("\n"),
);

console.log(`staged bundled skills at ${outDir}`);
console.log(`source: ${sourceSkills}`);
