#!/usr/bin/env node
import { spawnSync } from "node:child_process";
import {
  cpSync,
  existsSync,
  mkdirSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

function usage() {
  console.log(`Usage: node scripts/stage-dashboard-web-dist.mjs [options]

Builds or copies the hermes-agent-cn dashboard frontend and stages it under
static/dashboard/web_dist so Tauri bundles it into the desktop installer.

Options:
  --source <dir>          hermes-agent-cn checkout (default: ../hermes-agent-cn)
  --web-dist <dir>        Existing built web_dist directory to copy
  --out <dir>             Output dir (default: static/dashboard/web_dist)
  --skip-build            Fail if web_dist is missing instead of running npm build
`);
}

function argValue(flag, fallback = null) {
  const index = process.argv.indexOf(flag);
  if (index === -1) return fallback;
  const value = process.argv[index + 1];
  if (!value || value.startsWith("--")) {
    throw new Error(`${flag} requires a value`);
  }
  return value;
}

function hasFlag(flag) {
  return process.argv.includes(flag);
}

if (hasFlag("--help") || hasFlag("-h")) {
  usage();
  process.exit(0);
}

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const sourceRoot = resolve(
  repoRoot,
  argValue("--source", process.env.HERMES_AGENT_CN_SOURCE ?? "../hermes-agent-cn"),
);
const explicitWebDist = argValue("--web-dist", process.env.HERMES_DASHBOARD_WEB_DIST ?? null);
const outDir = resolve(repoRoot, argValue("--out", "static/dashboard/web_dist"));
const skipBuild = hasFlag("--skip-build") || process.env.HERMES_DASHBOARD_SKIP_BUILD === "1";

function run(command, args, options = {}) {
  const result = spawnSync(command, args, {
    stdio: "inherit",
    shell: false,
    ...options,
  });
  if (result.error) throw result.error;
  if (result.status !== 0) {
    throw new Error(`${command} ${args.join(" ")} exited with ${result.status}`);
  }
}

function capture(command, args, options = {}) {
  const result = spawnSync(command, args, {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
    shell: false,
    ...options,
  });
  if (result.status !== 0) return "";
  return result.stdout.trim();
}

function isDirectory(path) {
  try {
    return statSync(path).isDirectory();
  } catch {
    return false;
  }
}

function validateWebDist(path) {
  return existsSync(join(path, "index.html")) && isDirectory(join(path, "assets"));
}

function npmCommand() {
  return process.platform === "win32" ? "npm.cmd" : "npm";
}

function ensureDashboardDist() {
  if (explicitWebDist) {
    const dist = resolve(repoRoot, explicitWebDist);
    if (!validateWebDist(dist)) {
      throw new Error(`web_dist is invalid or missing index.html/assets: ${dist}`);
    }
    return dist;
  }

  const dist = join(sourceRoot, "hermes_cli", "web_dist");
  if (validateWebDist(dist)) return dist;

  if (skipBuild) {
    throw new Error(`dashboard web_dist is missing: ${dist}`);
  }

  const webDir = join(sourceRoot, "web");
  if (!existsSync(join(webDir, "package.json"))) {
    throw new Error(`hermes-agent-cn web package not found: ${webDir}`);
  }

  const npm = npmCommand();
  if (!isDirectory(join(webDir, "node_modules"))) {
    const installArgs = existsSync(join(webDir, "package-lock.json")) ? ["ci"] : ["install"];
    run(npm, installArgs, { cwd: webDir });
  }
  run(npm, ["run", "build"], { cwd: webDir });

  if (!validateWebDist(dist)) {
    throw new Error(`dashboard build did not produce a valid web_dist: ${dist}`);
  }
  return dist;
}

const sourceDist = ensureDashboardDist();
if (resolve(sourceDist) === resolve(outDir)) {
  throw new Error("source web_dist and output directory are the same path");
}

mkdirSync(dirname(outDir), { recursive: true });
rmSync(outDir, { recursive: true, force: true });
cpSync(sourceDist, outDir, { recursive: true });

const sourceCommit = isDirectory(join(sourceRoot, ".git"))
  ? capture("git", ["rev-parse", "HEAD"], { cwd: sourceRoot })
  : "";
writeFileSync(
  join(dirname(outDir), "README.generated.txt"),
  [
    "Generated by scripts/stage-dashboard-web-dist.mjs.",
    `source=${sourceRoot}`,
    sourceCommit ? `sourceCommit=${sourceCommit}` : null,
    `webDist=${sourceDist}`,
    "",
  ]
    .filter(Boolean)
    .join("\n"),
);

console.log(`staged dashboard web_dist at ${outDir}`);
console.log(`source: ${sourceDist}`);
