#!/usr/bin/env node
import {
  cpSync,
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

function usage() {
  console.log(`Usage: node scripts/stage-bundled-plugins.mjs [options]

Copies Hermes-CN-Core bundled plugins into static/bundled-plugins so Tauri
bundles them into the desktop installer. The desktop then syncs these files
into the managed runtime and exposes them to hermes dashboard via
HERMES_BUNDLED_PLUGINS.

Options:
  --source <dir>       Hermes-CN-Core checkout (default: ../Hermes-CN-Core, fallback: ../hermes-agent-cn)
  --plugins <dir>      Existing plugins directory to copy
  --out <dir>          Output dir (default: static/bundled-plugins)
`);
}

function argValue(flag, fallback = null) {
  const index = process.argv.indexOf(flag);
  if (index === -1) return fallback;
  const value = process.argv[index + 1];
  if (!value || value.startsWith("--")) {
    throw new Error(`${flag} requires a value`);
  }
  return value;
}

function hasFlag(flag) {
  return process.argv.includes(flag);
}

if (hasFlag("--help") || hasFlag("-h")) {
  usage();
  process.exit(0);
}

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
function defaultSourceRoot() {
  const preferred = resolve(repoRoot, "../Hermes-CN-Core");
  if (existsSync(preferred)) return preferred;
  return resolve(repoRoot, "../hermes-agent-cn");
}

const sourceRoot = resolve(
  repoRoot,
  argValue("--source", process.env.HERMES_AGENT_CN_SOURCE ?? defaultSourceRoot()),
);
const sourcePlugins = resolve(
  repoRoot,
  argValue("--plugins", process.env.HERMES_BUNDLED_PLUGINS_SOURCE ?? join(sourceRoot, "plugins")),
);
const outDir = resolve(repoRoot, argValue("--out", "static/bundled-plugins"));

function capture(command, args, options = {}) {
  const result = spawnSync(command, args, {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
    shell: false,
    ...options,
  });
  if (result.status !== 0) return "";
  return result.stdout.trim();
}

function isDirectory(path) {
  try {
    return statSync(path).isDirectory();
  } catch {
    return false;
  }
}

function walk(dir, visit) {
  if (!isDirectory(dir)) return;
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(full, visit);
    } else {
      visit(full, entry.name);
    }
  }
}

function hasPluginManifest(path) {
  let found = false;
  walk(path, (_full, name) => {
    if (name.toLowerCase() === "plugin.yaml" || name.toLowerCase() === "plugin.yml") {
      found = true;
    }
  });
  return found;
}

function validatePluginTree(path) {
  if (!isDirectory(path)) {
    throw new Error(`bundled plugins source is missing: ${path}`);
  }
  if (!hasPluginManifest(path)) {
    throw new Error(`bundled plugins source is missing plugin.yaml files: ${path}`);
  }

  const missingInits = [];
  const missingApis = [];
  walk(path, (full, name) => {
    const lower = name.toLowerCase();
    if (lower === "plugin.yaml" || lower === "plugin.yml") {
      const pluginDir = dirname(full);
      if (!existsSync(join(pluginDir, "__init__.py"))) {
        missingInits.push(pluginDir);
      }
      return;
    }
    if (lower !== "manifest.json" || dirname(full).split(/[\\/]/).pop() !== "dashboard") {
      return;
    }
    const manifest = JSON.parse(readFileSync(full, "utf8"));
    const api = typeof manifest.api === "string" ? manifest.api.trim() : "";
    if (api && !existsSync(join(dirname(full), api))) {
      missingApis.push(join(dirname(full), api));
    }
  });

  if (missingInits.length > 0) {
    throw new Error(
      `bundled plugins source has plugin manifests without __init__.py: ${missingInits
        .slice(0, 5)
        .join(", ")}`,
    );
  }
  if (missingApis.length > 0) {
    throw new Error(
      `bundled dashboard plugins declare missing api files: ${missingApis.slice(0, 5).join(", ")}`,
    );
  }
}

validatePluginTree(sourcePlugins);
if (resolve(sourcePlugins) === resolve(outDir)) {
  throw new Error("source plugins and output directory are the same path");
}

mkdirSync(dirname(outDir), { recursive: true });
rmSync(outDir, { recursive: true, force: true });
cpSync(sourcePlugins, outDir, { recursive: true });

const sourceCommit = isDirectory(join(sourceRoot, ".git"))
  ? capture("git", ["rev-parse", "HEAD"], { cwd: sourceRoot })
  : "";
writeFileSync(
  join(outDir, "README.generated.txt"),
  [
    "Generated by scripts/stage-bundled-plugins.mjs.",
    `source=${sourceRoot}`,
    sourceCommit ? `sourceCommit=${sourceCommit}` : null,
    `plugins=${sourcePlugins}`,
    "",
  ]
    .filter(Boolean)
    .join("\n"),
);

console.log(`staged bundled plugins at ${outDir}`);
console.log(`source: ${sourcePlugins}`);
